#!/usr/bin/env node

const http = require('http');
const fs = require('fs');
const path = require('path');

const LOGS_DIR = path.join(__dirname, '../logs');
const PORT = 19001;

// Ensure logs directory exists
if (!fs.existsSync(LOGS_DIR)) {
  fs.mkdirSync(LOGS_DIR, { recursive: true });
}

// Initialize log files with headers if they don't exist
const LOG_FILES = {
  info: path.join(LOGS_DIR, 'info.log'),
  warn: path.join(LOGS_DIR, 'warn.log'),
  error: path.join(LOGS_DIR, 'error.log'),
  critical: path.join(LOGS_DIR, 'critical.log'),
};

Object.entries(LOG_FILES).forEach(([level, filePath]) => {
  if (!fs.existsSync(filePath)) {
    fs.writeFileSync(filePath, `# ${level.toUpperCase()} LOGS\n# Generated by Dev Log Server\n\n`);
  }
});

function formatLogEntry(level, message) {
  const timestamp = new Date().toISOString();
  return `[${timestamp}] ${message}\n`;
}

function writeToFile(level, message) {
  const filePath = LOG_FILES[level];
  if (filePath) {
    try {
      fs.appendFileSync(filePath, message);
    } catch (error) {
      console.error('Failed to write to log file:', error);
    }
  }
}

const server = http.createServer((req, res) => {
  if (req.method === 'POST' && req.url === '/log') {
    let body = '';

    req.on('data', chunk => {
      body += chunk.toString();
    });

    req.on('end', () => {
      try {
        const { level, message } = JSON.parse(body);
        const logEntry = formatLogEntry(level, message);
        writeToFile(level, logEntry);
        
        res.writeHead(200, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ success: true }));
      } catch (error) {
        console.error('Failed to parse log:', error);
        res.writeHead(400, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ success: false, error: 'Invalid request' }));
      }
    });
  } else {
    res.writeHead(404);
    res.end('Not found');
  }
});

server.listen(PORT, () => {
  console.log(`\n=== Dev Log Server ===`);
  console.log(`Listening on http://localhost:${PORT}`);
  console.log(`Writing logs to: ${LOGS_DIR}`);
  console.log(`Press Ctrl+C to stop\n`);
});

// Handle graceful shutdown
process.on('SIGINT', () => {
  console.log('\nShutting down dev log server...');
  server.close();
  process.exit(0);
});
